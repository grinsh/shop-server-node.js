class User {
    constructor(id, name, email, password, city, street, houseNumber, phone, status, cart) { 
        this.id = id;
        this.name = name;
        this.email = email;
        this.password = password;
        this.city = city;
        this.street = street;
        this.houseNumber = houseNumber;
        this.phone = phone;
        this.status = status;
        this.cart = cart;
    }
}